  COPY INTO CAVIDA_POC.EXTRACTION.DB2_RECI0X_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/RECI0X/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

