-- =============================================================================
-- CA Vida POC - Pipeline Orchestration Task DAG
-- =============================================================================
-- This DAG orchestrates the full ELT pipeline in the correct execution order:
--   Stage 1: Extraction (17 parallel COPY INTO)
--   Stage 2: Transformation (sequential, dependency-ordered)
--   Stage 3: Load to DW (SCD Type 2)
--
-- To execute manually (without tasks), run scripts in the order below.
-- To automate, resume the ROOT task.
-- =============================================================================

USE SCHEMA CAVIDA_POC.ORCHESTRATION;

-- ─────────────────────────────────────────────────────────────────────────────
-- ROOT TASK (trigger point)
-- ─────────────────────────────────────────────────────────────────────────────
CREATE OR REPLACE TASK PIPELINE_ROOT
  WAREHOUSE = CAVIDA_POC_WH
  SCHEDULE = 'USING CRON 0 2 * * * Europe/Lisbon'  -- Daily at 02:00 Lisbon
  COMMENT = 'Root: triggers full ELT pipeline'
AS
  ALTER SESSION SET USE_CACHED_RESULT = FALSE;

-- ─────────────────────────────────────────────────────────────────────────────
-- STAGE 1: EXTRACTION (all 17 run in parallel after ROOT)
-- ─────────────────────────────────────────────────────────────────────────────
CREATE OR REPLACE TASK EXTRACT_ACTA00 WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_ACTA00_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/ACTA00/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_ACTA03 WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_ACTA03_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/ACTA03/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_ACTA0P WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_ACTA0P_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/ACTA0P/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_APOL00 WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_APOL00_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/APOL00/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_APOL03 WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_APOL03_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/APOL03/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_APOL0PH WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_APOL0PH_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/APOL0PH/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_APOL0P WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_APOL0P_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/APOL0P/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_APOLPA WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_APOLPA_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/APOLPA/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_ENTREL WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_ENTREL_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/ENTREL/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_ENTVIN WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_ENTVIN_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/ENTVIN/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_EVENTO WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_EVENTO_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/EVENTO/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_MOPEC1 WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_MOPEC1_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/MOPEC1/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

