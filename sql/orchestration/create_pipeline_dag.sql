  COPY INTO CAVIDA_POC.EXTRACTION.DB2_MOVIML_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/MOVIML/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

