  COPY INTO CAVIDA_POC.EXTRACTION.DB2_RECIBO_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/RECIBO/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1_RECIBO ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_RECPRO WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_RECPRO_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/RECPRO/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

CREATE OR REPLACE TASK EXTRACT_RECREL WAREHOUSE = CAVIDA_POC_WH AFTER PIPELINE_ROOT AS
  COPY INTO CAVIDA_POC.EXTRACTION.DB2_RECREL_POC FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/RECREL/ FILE_FORMAT = CAVIDA_POC.EXTRACTION.CSV_LATIN1 ON_ERROR = 'CONTINUE';

-- ─────────────────────────────────────────────────────────────────────────────
-- BARRIER: All extractions must complete before transformations start
-- ─────────────────────────────────────────────────────────────────────────────
CREATE OR REPLACE TASK BARRIER_EXTRACTION_COMPLETE
  WAREHOUSE = CAVIDA_POC_WH
  AFTER EXTRACT_ACTA00, EXTRACT_ACTA03, EXTRACT_ACTA0P, EXTRACT_APOL00,
       EXTRACT_APOL03, EXTRACT_APOL0PH, EXTRACT_APOL0P, EXTRACT_APOLPA,
       EXTRACT_ENTREL, EXTRACT_ENTVIN, EXTRACT_EVENTO, EXTRACT_MOPEC1,
       EXTRACT_MOVIML, EXTRACT_RECI0X, EXTRACT_RECIBO, EXTRACT_RECPRO,
       EXTRACT_RECREL
AS
  SELECT 'Extraction complete: all 17 tables loaded' AS STATUS;

-- ─────────────────────────────────────────────────────────────────────────────
-- STAGE 2: TRANSFORMATIONS (sequential, respecting dependencies)
-- ─────────────────────────────────────────────────────────────────────────────

-- UOE Path (sequential: 003 → 005)
CREATE OR REPLACE TASK TRANSFORM_UOE_003
  WAREHOUSE = CAVIDA_POC_WH
  AFTER BARRIER_EXTRACTION_COMPLETE
  COMMENT = 'UOE Step 3: FULL JOIN + RIGHT JOIN chain'
AS
  EXECUTE IMMEDIATE FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/scripts/INSPOL_LIFEUNITOFEXPOSURE_003.sql;

CREATE OR REPLACE TASK TRANSFORM_UOE_005
  WAREHOUSE = CAVIDA_POC_WH
  AFTER TRANSFORM_UOE_003
  COMMENT = 'UOE Step 5: LAG/LEAD window functions, parity logic'
AS
  EXECUTE IMMEDIATE FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/scripts/INSPOL_LIFEUNITOFEXPOSURE_005.sql;

-- LPT Path (sequential: 001 → 002 → 003)
CREATE OR REPLACE TASK TRANSFORM_LPT_001
  WAREHOUSE = CAVIDA_POC_WH
  AFTER BARRIER_EXTRACTION_COMPLETE
  COMMENT = 'LPT Step 1: Row explosion (1→6 UNION ALL) + 4-table join'
AS
  EXECUTE IMMEDIATE FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/scripts/INSPOL_LIFEPOLICYTRANS_001.sql;

CREATE OR REPLACE TASK TRANSFORM_LPT_002
  WAREHOUSE = CAVIDA_POC_WH
  AFTER TRANSFORM_LPT_001
  COMMENT = 'LPT Step 2: Employee lookup + X_PROCESSED_FLG state machine'
AS
  EXECUTE IMMEDIATE FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/scripts/INSPOL_LIFEPOLICYTRANS_002.sql;

CREATE OR REPLACE TASK TRANSFORM_LPT_003
  WAREHOUSE = CAVIDA_POC_WH
  AFTER TRANSFORM_LPT_002
  COMMENT = 'LPT Step 3: Append status-2 rows + RECREL + filter'
AS
  EXECUTE IMMEDIATE FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/scripts/INSPOL_LIFEPOLICYTRANS_003.sql;

-- ─────────────────────────────────────────────────────────────────────────────
-- BARRIER: All transformations must complete before DW load
-- ─────────────────────────────────────────────────────────────────────────────
CREATE OR REPLACE TASK BARRIER_TRANSFORM_COMPLETE
  WAREHOUSE = CAVIDA_POC_WH
  AFTER TRANSFORM_UOE_005, TRANSFORM_LPT_003
AS
  SELECT 'Transformation complete: staging tables ready for DW load' AS STATUS;

-- ─────────────────────────────────────────────────────────────────────────────
-- STAGE 3: DW LOAD (SCD Type 2 - can run in parallel)
-- ─────────────────────────────────────────────────────────────────────────────
CREATE OR REPLACE TASK LOAD_UOE
  WAREHOUSE = CAVIDA_POC_WH
  AFTER BARRIER_TRANSFORM_COMPLETE
  COMMENT = 'SCD2 load: LIFE_UNIT_OF_EXPOSURE (24.2M rows)'
AS
  EXECUTE IMMEDIATE FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/scripts/LOAD_LIFEUNITOFEXPOSURE.sql;

CREATE OR REPLACE TASK LOAD_LPT
  WAREHOUSE = CAVIDA_POC_WH
  AFTER BARRIER_TRANSFORM_COMPLETE
  COMMENT = 'Splitter load: LIFE_POLICY_TRANS + X_LIFE_POLICY_TRANS_HIST'
AS
  EXECUTE IMMEDIATE FROM @CAVIDA_POC.EXTRACTION.INPUT_DATA/scripts/LOAD_LIFEPOLICYTRANS.sql;

-- ─────────────────────────────────────────────────────────────────────────────
-- FINAL: Pipeline complete marker
-- ─────────────────────────────────────────────────────────────────────────────
CREATE OR REPLACE TASK PIPELINE_COMPLETE
  WAREHOUSE = CAVIDA_POC_WH
  AFTER LOAD_UOE, LOAD_LPT
AS
  INSERT INTO CAVIDA_POC.ORCHESTRATION.PIPELINE_MONITOR_LOG 
    (CHECK_TYPE, STATUS, DETAILS)
  VALUES (
    'PIPELINE_RUN',
    'COMPLETE',
    OBJECT_CONSTRUCT(
      'completed_at', CURRENT_TIMESTAMP()::VARCHAR,
      'uoe_rows', (SELECT COUNT(*) FROM CAVIDA_POC.DW.LIFE_UNIT_OF_EXPOSURE),
      'lpt_rows', (SELECT COUNT(*) FROM CAVIDA_POC.DW.LIFE_POLICY_TRANS)
    )
  );

-- ─────────────────────────────────────────────────────────────────────────────
-- RESUME ALL TASKS (bottom-up order required)
-- ─────────────────────────────────────────────────────────────────────────────
ALTER TASK PIPELINE_COMPLETE RESUME;
ALTER TASK LOAD_UOE RESUME;
ALTER TASK LOAD_LPT RESUME;
ALTER TASK BARRIER_TRANSFORM_COMPLETE RESUME;
ALTER TASK TRANSFORM_UOE_005 RESUME;
ALTER TASK TRANSFORM_LPT_003 RESUME;
