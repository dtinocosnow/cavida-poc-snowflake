ALTER TASK EXTRACT_ACTA00 RESUME;
ALTER TASK EXTRACT_ACTA03 RESUME;
ALTER TASK EXTRACT_ACTA0P RESUME;
ALTER TASK EXTRACT_APOL00 RESUME;
ALTER TASK EXTRACT_APOL03 RESUME;
ALTER TASK EXTRACT_APOL0PH RESUME;
ALTER TASK EXTRACT_APOL0P RESUME;
ALTER TASK EXTRACT_APOLPA RESUME;
ALTER TASK EXTRACT_ENTREL RESUME;
ALTER TASK EXTRACT_ENTVIN RESUME;
ALTER TASK EXTRACT_EVENTO RESUME;
